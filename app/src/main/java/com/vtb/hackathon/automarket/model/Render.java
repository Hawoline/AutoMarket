package com.vtb.hackathon.automarket.model;

public class Render {
    private Body wagon;
    private Body sedan;
    private Body liftback;
    private Body hatchback;

    public Body getWagon() {
        return wagon;
    }

    public void setWagon(Body wagon) {
        this.wagon = wagon;
    }

    public Body getSedan() {
        return sedan;
    }

    public void setSedan(Body sedan) {
        this.sedan = sedan;
    }

    public Body getLiftback() {
        return liftback;
    }

    public void setLiftback(Body liftback) {
        this.liftback = liftback;
    }

    public Body getHatchback() {
        return hatchback;
    }

    public void setHatchback(Body hatchback) {
        this.hatchback = hatchback;
    }
}
